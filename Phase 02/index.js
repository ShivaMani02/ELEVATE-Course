// JavaScript Data Types Examples

// String
let str = "Hello";

// Number
let num1 = 42;
let num2 = 3.14;

// Boolean
let isTrue = true;
let isFalse = false;

// Undefined
let x;

// Null
let y = null;

// Symbol
let sym = Symbol("id");

// BigInt
let bigInt = 12345678901234567890n;

// Output examples
console.log("String:", str);
console.log("Number:", num1, num2);
console.log("Boolean:", isTrue, isFalse);
console.log("Undefined:", x);
console.log("Null:", y);
console.log("Symbol:", sym);
console.log("BigInt:", bigInt);
